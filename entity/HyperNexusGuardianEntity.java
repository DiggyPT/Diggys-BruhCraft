	@Override
	protected void registerGoals() {
		super.registerGoals();
		this.goalSelector.addGoal(1, new MeleeAttackGoal(this, 2, false) {
			@Override
			protected double getAttackReachSqr(LivingEntity entity) {
				return this.mob.getBbWidth() * this.mob.getBbWidth() + entity.getBbWidth();
			}
		});
		this.targetSelector.addGoal(2, new NearestAttackableTargetGoal(this, Player.class, false, false));
		this.goalSelector.addGoal(3, new Goal() {
			{
				this.setFlags(EnumSet.of(Goal.Flag.MOVE));
			}

			public boolean canUse() {
				if (HyperNexusGuardianEntity.this.getTarget() != null && !HyperNexusGuardianEntity.this.getMoveControl().hasWanted()) {
					return true;
				} else {
					return false;
				}
			}

			@Override
			public boolean canContinueToUse() {
				return HyperNexusGuardianEntity.this.getMoveControl().hasWanted() && HyperNexusGuardianEntity.this.getTarget() != null && HyperNexusGuardianEntity.this.getTarget().isAlive();
			}

			@Override
			public void start() {
				LivingEntity livingentity = HyperNexusGuardianEntity.this.getTarget();
				Vec3 vec3d = livingentity.getEyePosition(1);
				HyperNexusGuardianEntity.this.moveControl.setWantedPosition(vec3d.x, vec3d.y, vec3d.z, 2);
			}

			@Override
			public void tick() {
				LivingEntity livingentity = HyperNexusGuardianEntity.this.getTarget();
				if (HyperNexusGuardianEntity.this.getBoundingBox().intersects(livingentity.getBoundingBox())) {
					HyperNexusGuardianEntity.this.doHurtTarget(livingentity);
				} else {
					double d0 = HyperNexusGuardianEntity.this.distanceToSqr(livingentity);
					if (d0 < 24) {
						Vec3 vec3d = livingentity.getEyePosition(1);
						HyperNexusGuardianEntity.this.moveControl.setWantedPosition(vec3d.x, vec3d.y, vec3d.z, 2);
					}
				}
			}
		});
		this.goalSelector.addGoal(4, new RandomStrollGoal(this, 2, 20) {
			@Override
			protected Vec3 getPosition() {
				RandomSource random = HyperNexusGuardianEntity.this.getRandom();
				double dir_x = HyperNexusGuardianEntity.this.getX() + ((random.nextFloat() * 2 - 1) * 16);
				double dir_y = HyperNexusGuardianEntity.this.getY() + ((random.nextFloat() * 2 - 1) * 16);
				double dir_z = HyperNexusGuardianEntity.this.getZ() + ((random.nextFloat() * 2 - 1) * 16);
				return new Vec3(dir_x, dir_y, dir_z);
			}
		});
		this.goalSelector.addGoal(5, new RandomLookAroundGoal(this));
		this.targetSelector.addGoal(6, new HurtByTargetGoal(this));
		this.goalSelector.addGoal(1, new RangedAttackGoal(this, 1.25, 20, 10f) {
			@Override
			public boolean canContinueToUse() {
				return this.canUse();
			}
		});
	}

